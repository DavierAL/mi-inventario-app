-- 6. RLS Policies
-- Everyone authenticated can read
CREATE POLICY "Authenticated users can read marcas_control" ON public.marcas_control
FOR SELECT USING (auth.role() = 'authenticated');

-- Only admin can write
CREATE POLICY "Only admin can modify marcas_control" ON public.marcas_control
FOR ALL USING (
  EXISTS (
    SELECT 1 FROM public.usuarios WHERE id = auth.uid() AND rol = 'admin'
  )
);
