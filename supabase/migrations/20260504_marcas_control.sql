-- 1. Crear tabla de control de marcas
CREATE TABLE IF NOT EXISTS public.marcas_control (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  nombre TEXT NOT NULL UNIQUE,
  dias_rango INT NOT NULL DEFAULT 30,
  ultimo_conteo TIMESTAMPTZ,
  inventariar BOOLEAN NOT NULL DEFAULT true,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- 2. Índices
CREATE INDEX IF NOT EXISTS idx_marcas_control_nombre ON public.marcas_control(nombre);
CREATE INDEX IF NOT EXISTS idx_marcas_control_inventariar ON public.marcas_control(inventariar);

-- 3. Trigger para actualizar updated_at automáticamente
CREATE OR REPLACE FUNCTION public.update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS update_marcas_control_updated_at ON public.marcas_control;
CREATE TRIGGER update_marcas_control_updated_at
  BEFORE UPDATE ON public.marcas_control
  FOR EACH ROW
  EXECUTE FUNCTION public.update_updated_at_column();

